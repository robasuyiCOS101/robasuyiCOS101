fn main() {
    //addition
    let sum = 5550 + 7310; 
    println!("sum is {}",sum);

    //subtraction
    let difference:f32 = 95.5 - 4.3;    // integer assigned to float value
    println!("difference value  is {}",difference);

    //multiplication
    let product:u32 = 4 * 30;
    println!("product value  is {}",product);

    //division
    let quotient:f64 = 56.7 / 32.2;     // double precision
    println!("division value  is {}",quotient);

    // remainder 
    let remainder = 43 % 5;
    println!("remainder value is {}",remainder);

}
